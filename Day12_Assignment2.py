# Program to convert a list to a tuple

# Given input list
listx = [5, 10, 7, 4, 15, 3]

# Convert list to tuple
result = tuple(listx)  # Using the tuple() constructor

# Display the result
print("Before:",listx)
print("After:", result)

'''
Output:
before [5, 10, 7, 4, 15, 3]
After: (5, 10, 7, 4, 15, 3)
'''
